def houseRobber(nums, i, suma):
    if  i == -1:
        return max(houseRobber(nums, 0, suma),
                   houseRobber(nums, 1, suma))

    if (i>=len(nums)):
        return suma

    return max(houseRobber(nums, i+2, suma+nums[i]),
                houseRobber(nums, i+3, suma+nums[i]))


